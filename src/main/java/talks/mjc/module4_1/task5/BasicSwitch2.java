package talks.mjc.module4_1.task5;

public class BasicSwitch2
{
    public static void main(String[] args) {
        int a = 20;
        switch (a) {
            default:
                System.out.println("default");
            case 7:
                System.out.println("seven");
            case 10:
                System.out.println("ten");
            case 20:
                System.out.println("twenty");
        }
    }
}
